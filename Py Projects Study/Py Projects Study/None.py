"""

Tipo None

O tipo none representa o tipo sem tipo, pode ser reconhecido como tipo vazio
porém dizer que é um tipo sem tipo seria o mais apropriado.

O tipo None é sempre específicado com a primeira letra maiúscula.

Podemos utilizar o None quando queremos criar uma variável e inicializa-la com um tipo sem tipo
antes de receber um valor final.

O tipo None em python é sempre considerado como falso

"""
numeros = None

print(numeros)
print(type(numeros))

numeros = 1.44, 1.34, 6.56

print(numeros)
print(type(numeros))
